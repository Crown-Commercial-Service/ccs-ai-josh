import os
from dotenv import load_dotenv
from langchain_openai import AzureChatOpenAI
from langchain.schema import SystemMessage, HumanMessage

load_dotenv()

# Connect to model on Azure
llm = AzureChatOpenAI(
    azure_endpoint=os.getenv("AZURE_OPENAI_ENDPOINT"),
    openai_api_key=os.getenv("AZURE_OPENAI_KEY"),
    azure_deployment=os.getenv("DEPLOYMENT_NAME"),
    openai_api_version=os.getenv("AZURE_OPENAI_API_VERSION"),
    temperature=0.0
)

# System prompt for LLM-as-a-judge


def score_correctness(question:str, generated_answer:str, reference_answer:str) -> int:
    """Uses an LLM-as-a-judge approach to score the similarity between a generated answer and reference answer.
    Args:
        question: the question from the user/requestor
        generated_answer: the answer generated by the LLM being evaluated
    Returns:
        score: the correctness score, from 1 (poorest) to 10 (best)
    """
    system_prompt = """
    You are an expert evaluator for question-answering systems. For each input, you are given:
    - The original question
    - The generated answer from a system to be evaluated
    - The reference (correct) answer

    Your task is to compare the generated answer to the reference answer and assign a score from 1 to 10, where:
    10 means the generated answer matches the reference answer perfectly in content, completeness, and factual accuracy;
    1 means the generated answer is completely incorrect or irrelevant;
    Intermediate numbers reflect partial or incomplete matches.

    Provide ONLY the integer score, and nothing else.
    """
    user_prompt = f"""Question: {question}

    Reference answer: {reference_answer}

    Generated answer: {generated_answer}
    """
    messages = [
        SystemMessage(content=system_prompt),
        HumanMessage(content=user_prompt)
    ]
    response = llm.invoke(messages)
    score_str = response.content.strip()
    try:
        score = int(score_str)
        assert 1 <= score <= 10
    except Exception as e:
        raise ValueError(f"Model returned unexpected score: '{score_str}'") from e
    return score

def score_retrieval(question:str, context:list) -> int:
    """Uses an LLM-as-a-judge approach to score the similarity between a generated answer and reference answer.
    Args:
        question: the question from the user/requestor
        context: the text from the retrieved content, as a list
    Returns:
        score: the correctness score, from 1 (poorest) to 10 (best)
    """
    system_prompt = """
    You are an expert evaluator for question-answering systems. For each input, you are given:
    - The original question
    - The retrieved context/document that the system would use for answering

    Your task is to assess whether the retrieved context is likely to contain the information needed to answer the question correctly. 

    Assign a score from 1 to 10:
    10 means the context contains all the information required to answer the question;
    1 means it contains none of the required information.
    Intermediate numbers reflect partial coverage or relevance.

    Provide ONLY the integer score, and nothing else.
    """
    user_prompt = f"""Question: {question}

    Retrieved context: {context}

    How likely is it that the retrieved context contains the necessary information to answer the question?
    Reply with a score from 1 (not at all) to 10 (completely covered). Only provide the score.
    """
    messages = [
        SystemMessage(content=system_prompt),
        HumanMessage(content=user_prompt)
    ]
    response = llm.invoke(messages)
    score_str = response.content.strip()
    try:
        score = int(score_str)
        assert 1 <= score <= 10
    except Exception as e:
        raise ValueError(f"Model returned unexpected score: '{score_str}'") from e
    return score

def score_groundedness(context: list, generated_answer: str) -> int:
    """Uses an LLM-as-a-judge approach to score the groundedness of a generated answer with respect to its context.
    Args:
        context: the text from the retrieved content, as a list
        generated_answer: the answer generated by the LLM being evaluated
    Returns:
        score: the correctness score, from 1 (poorest) to 10 (best)
    """
    system_prompt = """
    You are an expert evaluator for question-answering systems. For each input, you are given:
    - The original question
    - The context/retrieved document provided to the system
    - The generated answer from the system

    Your task is to determine how well the generated answer is supported by information from the context. The answer should not introduce facts not present in the context. 

    Assign a score from 1 to 10:
    10 means the answer is fully supported by the context and contains no extra or hallucinated content;
    1 means the answer is completely unsupported or fabricated, or uses information that does not appear in the context.
    Intermediate numbers reflect partial groundedness.

    Provide ONLY the integer score, and nothing else.
    """
    user_prompt = f"""
    Context/retrieved document: {context}

    Generated answer: {generated_answer}

    Score the groundedness of the generated answer in the context from 1 (not grounded) to 10 (fully grounded). 
    Reply with only the score.
    """
    messages = [
        SystemMessage(content=system_prompt),
        HumanMessage(content=user_prompt)
    ]
    response = llm.invoke(messages)
    score_str = response.content.strip()
    try:
        score = int(score_str)
        assert 1 <= score <= 10
    except Exception as e:
        raise ValueError(f"Model returned unexpected score: '{score_str}'") from e
    return score

def evaluate_response(question, response, context, ref_answer) -> dict:
    """Evaluates a response by calculating correctness, retrieval accuracy and groundedness.
    """
    correctness_score = score_correctness(
        question=question,
        generated_answer=response,
        reference_answer=ref_answer
    )
    retrieval_score = score_retrieval(
        question=question,
        context=context
    )
    groundedness_score = score_groundedness(
        context=context,
        generated_answer=response
    )
    results = {
        "correctness": correctness_score,
        "retrieval": retrieval_score,
        "groundedness": groundedness_score
    }
    return results

if __name__ == "__main__":
    q = "What is the capital of France?"
    ref_ans = "The capital of France is Paris."
    gen_ans_correct = "Paris is the capital of France."
    gen_ans_incorrect = "Paris is the capital of Spain"
    chunk1 = "European capitals: UK=London, France=Paris, Spain=Madrid, Germany=Berlin"
    chunk2 = "French cities: Marseille, Paris (capital), Lyon, Versailles"
    chunk3 = "Spanish cities: Barcelona, Madrid, Seville"
    results = evaluate_response(q, gen_ans_incorrect, [chunk1, chunk2], ref_ans)
    print(f"Correctness = {results["correctness"]}")
    print(f"Retrieval = {results["retrieval"]}")
    print(f"Groundedness = {results["groundedness"]}")